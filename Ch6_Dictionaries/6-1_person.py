# Defines a dictionary storing different information about a person
# using key-value pairs.
person = {'first_name': 'ash',
          'last_name': 'ketchum',
          'age': 26,
          'town': 'pallet'}

# Prints value associated with each given key of the dictionary.
print(person['first_name'])
print(person['last_name'])
print(person['age'])
print(person['town'])
